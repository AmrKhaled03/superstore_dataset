for col in df.columns:
     if pd.api.types.is_numeric_dtype(df[col]):
         df[col] = df[col].fillna(df[col].mean())
     elif pd.api.types.is_object_dtype(df[col]):
         df[col] = df[col].fillna(df[col].mode()[0])
     else:
         df[col] = df[col].fillna(df[col].mode()[0])

